    private void bteditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditActionPerformed
      try {
            if (dsdh.capNhatDH(txtmaDH.getText(), txttenKH.getText(), txtdiaChi.getText(), txttenSP.getText(), Float.parseFloat(txtDonGia.getText()), Integer.parseInt(txtsoLuong.getText()), DonHang.chuyenChuoiThanhNgay(txtngayDat.getText()), txttrangThai.getText())) {
                for(DonHang x: dh){
                    if(x.getMaDH().equalsIgnoreCase(txtmaDH.getText())){
                        x.setTenKH(txttenKH.getText());
                        x.setDiaChi(txtdiaChi.getText());
                        x.setTenSP(txttenSP.getText());
                        x.setDonGia(Float.parseFloat(txtDonGia.getText()));
                        x.setSoLuong(Integer.parseInt(txtsoLuong.getText()));
                        x.setNgayDat(DonHang.chuyenChuoiThanhNgay(txtngayDat.getText()));
                        x.setTrangThai(txttrangThai.getText());
                    }
                }
                loadData(dsdh.getDSDH());
                resetLabel();
                addmess("Đã sửa thành công");
            } else {
                addmess("Mã đơn hàng không tồn tại");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(btedit, e);
            addmess(e.getMessage());
        } 
    }//GEN-LAST:event_bteditActionPerformed

